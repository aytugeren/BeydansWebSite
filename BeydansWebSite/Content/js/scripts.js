// Get the video
var video = document.getElementById("myVideo");

// Get the button
var btn = document.getElementById("myBtn");

// Pause and play the video, and change the button text
function myFunction() {
	if (video.paused) {
		video.play();
		btn.innerHTML = "Durdur";
	} else {
		video.pause();
		btn.innerHTML = "Devam Et";
	}
}

var volumeOpenButton = document.getElementById("volumeUp");
var volumeCloseButton = document.getElementById("volumeOff");

function volumeUpAndOff() {
	if (video.muted) {
		video.muted = false;
		volumeOpenButton.classList.remove("hidden");
		volumeCloseButton.classList.add("hidden");
	}
	else {
		video.muted = true;
		volumeOpenButton.classList.add("hidden");
		volumeCloseButton.classList.remove("hidden");
    }
}

